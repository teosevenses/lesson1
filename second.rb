Площадь треугольника. Площадь треугольника можно вычислить, зная его основание 
(a) и высоту (h) по формуле: 1/2*a*h. 
Программа должна запрашивать основание и высоту треугольника и возвращать его площадь.

puts "Введите длину основания"
a = gets.chomp.to_i

puts "Введите длину высоты"
h = gets.chomp.to_i

puts "S == #{a*h/2}"

